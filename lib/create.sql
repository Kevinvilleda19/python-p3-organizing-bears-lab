CREATE TABLE bears (
  id INTEGER PRIMARY KEY,     -- Unique identifier for each bear
  name TEXT,                  -- Bear's name
  age INTEGER,                -- Bear's age
  sex TEXT,                   -- Bear's sex (male/female)
  color TEXT,                 -- Color of the bear
  temperament TEXT,           -- Bear's temperament
  alive BOOLEAN               -- Whether the bear is alive or not (true/false)
);
